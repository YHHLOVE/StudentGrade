package beans;

public class Student {
	
	private String studentId;
	private String studentName;
	private String studentSex;
	private String studentClass;
	private String passWord;
	public Student() {
	
	}
	public Student(String studentId, String studentName, String studentSex, String studentClass, String passWord) {
		this.studentId = studentId;
		this.studentName = studentName;
		this.studentSex = studentSex;
		this.studentClass = studentClass;
		this.passWord = passWord;
	}
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getStudentSex() {
		return studentSex;
	}
	public void setStudentSex(String studentSex) {
		this.studentSex = studentSex;
	}
	public String getStudentClass() {
		return studentClass;
	}
	public void setStudentClass(String studentClass) {
		this.studentClass = studentClass;
	}
	public String getPassWord() {
		return passWord;
	}
	public void setPassWord(String passWord) {
		this.passWord = passWord;
	}
	@Override
	public String toString() {
		return "Student [studentId=" + studentId + ", studentName=" + studentName + ", studentSex=" + studentSex
				+ ", studentClass=" + studentClass + ", passWord=" + passWord + "]";
	}
	
}
